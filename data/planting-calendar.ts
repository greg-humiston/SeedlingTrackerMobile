function monthInWindow(month: number, window: [number, number]): boolean {
  const [start, end] = window;
  if (start <= end) return month >= start && month <= end;
  // Wraps around year (e.g., [11, 2])
  return month >= start || month <= end;
}

export function getPlantsForZoneAndMonth(zone: ZoneBand, month: number): PlantEntry[] {
  return PLANTS.filter((plant) => {
    const windows = plant.plantingWindows[zone];
    return windows.some((w) => monthInWindow(month, w));
  });
}
